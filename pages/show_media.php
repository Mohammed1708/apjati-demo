                Jakarta - The management of the Central Executive Board (DPP) of the Association of Indonesian Migrant Workers Services Companies (APJATI) met with the Minister
                For Food Industry, Commodity and Regional Development Malaysia, His
                Honor Dato SRI Dr. Stephen Rundi Utom at the Sheraton Hotel, Jakarta,
                Monday 06/01/25.
                The meeting was also attended by, among others; His Honor Tan Sri William Mawan
                Ikom as Advisor in the office of the Premier of Sarawak/ Chairman of SALCRA,
                His Honor Datuk Malcom Mussen Lamoh as Deputy Minister for International Trade,
                Industry and Investment,
                Sarawak, His Honor Encik Miro Simuh as ADUN N.18. Serumbu,
                Happy Datu Edwin Abit as Board Member of SALCRA Jaya
                Sdn Bhd., Encik Stephen Ruin as Private Secretary of the Ministry for Food
                Industry, Community and Regional Development, Sarawak, Tuan Sikin
                Sentok as Deputy General Manager of "SALCRA", Encik Debrin Peter
                Kinias Legal and Secretarial Services Manager "SALCRA", Encik Will
                Paul Bilun as Chief Operating Officer (Mills) "SALCRA", Mr Peter
                Manjan as Chief Operating Officer (Sarawak Fertilizer Sdn Bhd).
                APJATI General Chair Said Saleh Alwaini said that this meeting
                is a friendship and introduction to the Minister for Food Industry,
                Commodity and Regional Development Malaysia with APJATI administrators
                new under his leadership.
                "On the occasion of the gathering, we also discussed the potential for
                placement and increased protection of Indonesian Migrant Workers in Malaysia,
                especially in the East Malaysia region. Where recently the placement process
                has been felt to be very time-consuming, both in processing Job orders and the
                visa issuance process which has been felt to take so long, said Said. The General
                Chairperson of Apjati "Said Saleh" conveyed technical issues that need special
                attention from the Malaysian Government in this case,
                His Excellency Dato SRI Dr. Stephen Rundi Utom as Minister for Food Industry,
                Community and Regional Development Malaysia, including; The issuance of Calling
                Visas is too long, The large number of Illegal Workers entering Sarawak,
                Opening up greater opportunities to place Indonesian Migrant Workers in East
                Malaysia regarding West Malaysia still being closed to the placement of
                Migrant Workers, and also asking for certainty about the increase in the minimum
                salary of RM.1700. On the first occasion, His Excellency Dato SRI Dr. Stephen Rundi
                Utom said that he would immediately call the relevant parties in Malaysia to
                find a way out so that the Visa Colling process for Workers who will enter Sarawak
                can be accelerated, because according to him this is the second time he has come to
                Indonesia and heard more or less the same issue. The Honorable Dato SRI Dr. Stephen Rundi
                Utom also expressed his gratitude to APJATI and SALCRA for initiating the meeting and discussing
                the issue of Illegal Workers who enter Sarawak, he said that the problem of Illegal workers
                must be discussed with the Indonesian government and the Malaysian Government, but he really
                hopes that SALCRA will not accept workers who enter Sarawak illegally.
                Furthermore, Dato SRI Dr. Stephen Rundi Utom said that SALCRA can ensure protection for Indonesian Migrant Workers
                who are employed and not employ illegal workers. The APJATI General Chairperson closed
                the dialogue with the hope that "the cooperation between P3MI members of APJATI and
                SALCRA Jaya Sdn Bhd can run effectively and efficiently, and it is hoped that more prospective migrant workers can participate in this program," he concluded. "Infokom APJATI"
